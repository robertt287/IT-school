package session3.challenges;

//7. Write a program where you perform operations between an integer and a double. Display the result.
public class challenge7 {
    public static void main(String[] args) {
        int integerNumber = 5;
        double doubleNumber = 2.5;

        // Addition
        double resultAddition = integerNumber + doubleNumber;
        System.out.println("Addition result: " + resultAddition);

        // Subtraction
        double resultSubtraction = integerNumber - doubleNumber;
        System.out.println("Subtraction result: " + resultSubtraction);

        // Multiplication
        double resultMultiplication = integerNumber * doubleNumber;
        System.out.println("Multiplication result: " + resultMultiplication);

        // Division
        double resultDivision = integerNumber / doubleNumber;
        System.out.println("Division result: " + resultDivision);
    }
}
